   <p >La panificadora Tabora, fue creada el 16 de septiembre del año 1,986 a iniciativa de cuatro hermanos y su padre con el propósito de producir pan, iniciando sus operaciones con cinco empleados.</p>
  <p>En el transcurso de los siguientes 12 años debido a factores como de infraestructura no adecuada, falta de maquinaria, equipo industrializado, y falta de recursos financieros para la producción de pan el crecimiento de la empresa fue lento.</p>
  <p>Fue hasta el año 1,999 que por unanimidad de sus fundadores se decide construir una sociedad de Responsabilidad Limitada tomando el nombre de PANIFICADORA TABORA S. de. RL de CV. Es a partir de ese momento que la empresa tomo un giro diferente y se toman medidas eficaces como inversión de maquinaria y equipo industrial moderno y adecuado, importado especialmente de la republica de Argentina a una compañía especializada en la fabricación de equipo y maquinaria para la industria de panificación, se remodelaron en totalidad las instalaciones físicas creando así un plantel para fabricar un producto que cubre estándares de calidad exigidos por el mercado.
  Además se creo un estructura organizacional por departamentos, creándose los departamentos de:</p>
  <h6 class="font-weight-bold">• Empaque</h6>
  <h6 class="font-weight-bold">• Producción</h6>
  <h6 class="font-weight-bold">• Ventas</h6>
  <h6 class="font-weight-bold">• Administración</h6>
  <p>Contrataron personal con conocimientos básicos para dirigir cada uno de los cargos existentes, se nombro una gerencia administrativa que desde un inicio a sido dirigida exclusivamente por los socios con periodos rotativos de un año.
  Desde entonces hasta la actualidad se llevan controles administrativos estrictos.</p>
  <p>Uno de los principales objetivos de la PANIFICADORA TABORA S. de. RL de CV. es generar empleo a personas de escasos recursos económicos y con niveles de educación bajos contribuyendo así a bajar los índices de desempleo que agobia esta región y sobre todo a mejorar el estatus de vida de un sector privilegiado ya sea de forma directa o indirecta. <hr style="background-color:#2E64FE"> </p>

